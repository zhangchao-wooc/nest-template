export const customRolePrefix = (role: number | string): string =>
  `role::${role}`;
export const customResourcePrefix = (resource: number | string): string =>
  `resource::${resource}`;
export const customDomainPrefix = (domain: number | string): string =>
  `domain::${domain}`;
export const customUserPrefix = (user: number | string): string =>
  `user::${user}`;
